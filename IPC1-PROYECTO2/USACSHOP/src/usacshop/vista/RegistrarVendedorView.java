    private void generarCodigoAutomatico(){     //para generar el codigo automatico del vendedor
        int contador = 1;
        try (BufferedReader reader = new BufferedReader(new FileReader("vendedores.txt"))){
            String linea;
            while ((linea = reader.readLine()) != null){
                contador++;
            }
        } catch (IOException e){
            
        }
        txtCodigo.setText("V" + contador);
    }
    
